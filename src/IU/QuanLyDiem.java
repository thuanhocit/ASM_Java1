void sqlServer2table() {
    try {
        // Sử dụng một câu truy vấn SQL để lấy 3 sinh viên có điểm trung bình cao nhất
        String sql = "SELECT TOP 3 G.ID, S.MaSV, S.Hoten, TiengAnh, TinHoc, GDTC " +
                     "FROM GRADE G JOIN STUDENTS S ON G.MaSV = S.MaSV " +
                     "ORDER BY (TiengAnh + TinHoc + GDTC) / 3.0 DESC";
        
        stm = conn.createStatement(); // Khởi tạo statement
        rs = stm.executeQuery(sql); // Thực hiện truy vấn
        DefaultTableModel model = (DefaultTableModel) tblTopSV.getModel();
        model.setRowCount(0); // Xóa các dòng hiện tại trong bảng
        
        while (rs.next()) {
            int id = rs.getInt("ID");
            String MaSV = rs.getString("MaSV");
            String Hoten = rs.getString("Hoten");
            int tiengAnh = rs.getInt("TiengAnh");
            int TinHoc = rs.getInt("TinHoc");
            int GDTC = rs.getInt("GDTC");
            float diemTB = (tiengAnh + TinHoc + GDTC) / 3.0f;
            
            // Thêm dòng mới vào bảng
            model.addRow(new Object[]{id, MaSV, Hoten, tiengAnh, TinHoc, GDTC, diemTB});
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}


    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      try {
        // Lấy mã sinh viên từ trường nhập liệu
        String MaSV = txtTimMaSV.getText();       
        // Tạo câu lệnh SQL để lấy thông tin sinh viên từ cơ sở dữ liệu
        String sql = "SELECT S.Hoten, G.TiengAnh, G.TinHoc, G.GDTC "
                   + "FROM Students S JOIN Grade G ON S.MaSV = G.MaSV "
                   + "WHERE S.MaSV = '" + MaSV + "'";
        
        // Thực thi truy vấn SQL và lấy kết quả
        rs = stm.executeQuery(sql);       
        // Nếu có kết quả
        if (rs.next()) {
            // Lấy thông tin sinh viên từ ResultSet
            String hoTen = rs.getString("Hoten");
            int tiengAnh = rs.getInt("TiengAnh");
            int tinHoc = rs.getInt("TinHoc");
            int GDTC = rs.getInt("GDTC");
            
            // Hiển thị thông tin sinh viên trên giao diện
            txtHoTen.setText(hoTen);
            txtMaSV.setText(MaSV);
            txtTiengAnh.setText(String.valueOf(tiengAnh));
            txtTinHoc.setText(String.valueOf(tinHoc));
            txtGDTC.setText(String.valueOf(GDTC));
        } else {
            // Nếu không có kết quả, thông báo không tìm thấy sinh viên
            JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên có mã " + MaSV);
            xoaForm();
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi tìm kiếm sinh viên: " + e.getMessage());
        
    }
    }//GEN-LAST:event_btnSearchActionPerformed


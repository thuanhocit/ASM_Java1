    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
 String MaSV = txtMaSV.getText();
    String TiengAnh = txtTiengAnh.getText();
    String TinHoc = txtTinHoc.getText();
    String GDTC = txtGDTC.getText();
    try {
        // Truy vấn cơ sở dữ liệu để lấy tên sinh viên tương ứng với mã sinh viên được nhập
        String query = "SELECT Hoten FROM Students WHERE MaSV = ?";
        PreparedStatement pstmt = conn.prepareStatement(query);
        pstmt.setString(1, MaSV);
        ResultSet result = pstmt.executeQuery();
        
        // Nếu tìm thấy sinh viên với mã sinh viên được nhập
        if (result.next()) {
            String hoTen = result.getString("Hoten");
            // Hiển thị tên sinh viên lên giao diện người dùng
            txtHoTen.setText(hoTen);
            
            // Tạo câu lệnh INSERT để thêm điểm cho sinh viên vào cơ sở dữ liệu
            String sql = "INSERT INTO Grade (MaSV, TiengAnh, TinHoc, GDTC) VALUES (?, ?, ?, ?)";
            // Tạo PreparedStatement để thực thi câu lệnh INSERT
            PreparedStatement pstmtInsert = conn.prepareStatement(sql);
            pstmtInsert.setString(1, MaSV);
            pstmtInsert.setString(2, TiengAnh);
            pstmtInsert.setString(3, TinHoc);
            pstmtInsert.setString(4, GDTC);
            // Thực thi câu lệnh INSERT
            int rowsInserted = pstmtInsert.executeUpdate();
            
            // Nếu thêm điểm thành công
            if (rowsInserted > 0) {
                // Hiển thị thông báo khi thêm điểm thành công
                JOptionPane.showMessageDialog(this, "Thêm điểm cho SV thành công!");
                
                // Tính lại điểm trung bình và hiển thị trên nhãn lblDiemTB
                sqlServer2table(); // Cập nhật dữ liệu trên bảng
                float diemTB = calculateDiemTB(); // Tính điểm trung bình mới
                lblDiemTB.setText("Điểm TB: " + diemTB); // Hiển thị điểm trung bình mới
            } else {
                // Hiển thị thông báo khi không thể thêm điểm
                JOptionPane.showMessageDialog(this, "Không thể thêm điểm cho SV!");
            }
        } else {
            // Nếu không tìm thấy sinh viên với mã sinh viên được nhập
            JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên với Mã SV này!");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi thêm điểm: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSaveActionPerformed

